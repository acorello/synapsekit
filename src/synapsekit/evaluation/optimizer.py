"""PromptOptimizer — score prompt variants against an existing @eval_case suite."""

from __future__ import annotations

import importlib.util
import inspect
import json
import logging
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Any

log = logging.getLogger(__name__)


@dataclass
class PromptCandidate:
    text: str
    score: float
    cost_usd: float
    metadata: dict | None = None


def _parse_json_list(text: str) -> list[str]:
    """Extract a JSON string list from LLM output, tolerating markdown fences."""
    text = re.sub(r"```(?:json)?\s*", "", text).strip().rstrip("`").strip()
    try:
        result = json.loads(text)
        if isinstance(result, list):
            return [str(v) for v in result if v]
    except json.JSONDecodeError:
        pass
    m = re.search(r"\[.*?\]", text, re.DOTALL)
    if m:
        try:
            result = json.loads(m.group())
            if isinstance(result, list):
                return [str(v) for v in result if v]
        except json.JSONDecodeError:
            pass
    return []


class PromptVariantRunner:
    """Runs all eval cases for a single prompt variant and returns a scored candidate."""

    def __init__(self, eval_cases: list[tuple[str, Any]], metric: str) -> None:
        self._eval_cases = eval_cases
        self._metric = metric

    async def run_variant(self, prompt: str) -> PromptCandidate:
        scores: list[float] = []
        total_cost = 0.0

        for _name, fn in self._eval_cases:
            try:
                sig = inspect.signature(fn)
                inject = "prompt" in sig.parameters
                if inspect.iscoroutinefunction(fn):
                    result = await fn(prompt=prompt) if inject else await fn()
                else:
                    result = fn(prompt=prompt) if inject else fn()

                if not isinstance(result, dict):
                    result = {"score": float(result) if result is not None else 0.0}

                scores.append(float(result.get(self._metric) or 0.0))
                total_cost += float(result.get("cost_usd") or 0.0)
            except Exception:
                scores.append(0.0)

        avg_score = sum(scores) / len(scores) if scores else 0.0
        return PromptCandidate(
            text=prompt,
            score=avg_score,
            cost_usd=total_cost,
            metadata={"n_cases": len(self._eval_cases), "scores": scores},
        )


class PromptOptimizer:
    """Optimize a prompt by scoring variants against an ``@eval_case`` suite.

    Variants are either supplied directly or generated by the LLM.
    The eval suite is discovered from ``eval_suite`` (a file or directory
    following the same ``eval_*.py`` / ``*_eval.py`` convention as the CLI).
    Optimization stops early if ``budget_usd`` is exceeded.

    After :meth:`run`, all evaluated candidates are in :attr:`candidates`
    ranked by score descending.

    Usage::

        optimizer = PromptOptimizer(
            llm=llm,
            eval_suite="evals/",
            metric="score",
            n_variants=5,
            budget_usd=1.0,
        )
        best = await optimizer.run(
            base_prompt="Answer the question:",
            instructions="Make it more concise and direct.",
        )
        print(best.text, best.score)
    """

    def __init__(
        self,
        llm: Any,
        eval_suite: str,
        metric: str,
        n_variants: int = 5,
        budget_usd: float | None = None,
        variants: list[str] | None = None,
    ) -> None:
        self.llm = llm
        self.eval_suite = eval_suite
        self.metric = metric
        self.n_variants = n_variants
        self.budget_usd = budget_usd
        self.variants = variants
        self.candidates: list[PromptCandidate] = []

    # ------------------------------------------------------------------
    # Eval suite loading
    # ------------------------------------------------------------------

    def _load_eval_cases(self) -> list[tuple[str, Any]]:
        """Load all ``@eval_case``-decorated functions from :attr:`eval_suite`.

        ``eval_suite`` is resolved relative to the **current working directory**
        at call time.  Pass an absolute path to avoid ambiguity.
        """
        root = Path(self.eval_suite)
        files: list[Path] = []
        if root.is_file():
            files = [root]
        else:
            files.extend(root.rglob("eval_*.py"))
            files.extend(root.rglob("*_eval.py"))

        cases: list[tuple[str, Any]] = []
        for filepath in sorted(set(files)):
            spec = importlib.util.spec_from_file_location(filepath.stem, filepath)
            if spec is None or spec.loader is None:
                continue
            mod = importlib.util.module_from_spec(spec)
            try:
                spec.loader.exec_module(mod)  # type: ignore[union-attr]
            except Exception as exc:
                log.warning("Skipping eval file %s — import failed: %s", filepath, exc)
                continue
            for name in dir(mod):
                obj = getattr(mod, name)
                if callable(obj) and hasattr(obj, "_eval_case_meta"):
                    cases.append((name, obj))

        return cases

    # ------------------------------------------------------------------
    # Variant generation
    # ------------------------------------------------------------------

    async def _generate_variants(self, base_prompt: str, instructions: str) -> list[str]:
        generation_prompt = (
            f"Given the base prompt:\n\n{base_prompt}\n\n"
            f"Improve it based on:\n\n{instructions}\n\n"
            f"Generate {self.n_variants} variations.\n"
            "Each should be concise and meaningfully different.\n"
            "Return ONLY a JSON list of prompts."
        )
        try:
            raw = await self.llm.generate(generation_prompt)
            variants = _parse_json_list(raw)
            if variants:
                return variants[: self.n_variants]
        except Exception:
            pass
        return [base_prompt]

    # ------------------------------------------------------------------
    # Main entry point
    # ------------------------------------------------------------------

    async def run(self, base_prompt: str, instructions: str) -> PromptCandidate:
        """Score all variants and return the best-scoring candidate.

        Stops early if cumulative ``cost_usd`` from eval results reaches
        ``budget_usd``.  All evaluated candidates are stored in
        :attr:`candidates`, sorted by score descending.
        """
        eval_cases = self._load_eval_cases()
        runner = PromptVariantRunner(eval_cases, self.metric)

        prompt_variants: list[str] = (
            list(self.variants)
            if self.variants
            else await self._generate_variants(base_prompt, instructions)
        )

        total_cost = 0.0
        self.candidates = []

        for variant in prompt_variants:
            candidate = await runner.run_variant(variant)
            self.candidates.append(candidate)
            total_cost += candidate.cost_usd

            if self.budget_usd is not None and total_cost >= self.budget_usd:
                break

        self.candidates.sort(key=lambda c: c.score, reverse=True)

        if not self.candidates:
            return PromptCandidate(text=base_prompt, score=0.0, cost_usd=0.0)

        return self.candidates[0]
